void setColor(unsigned short r, unsigned short g, unsigned short b){
	unsigned short g1=0;
	unsigned short g2=0;
	r= (unsigned short)(r& 0xf8)|(0x00);
	g1= (unsigned short)(g& 0xE0)|(0x00);
	g2= (unsigned short)(g& 0x1C)|(0x00);
	b= (unsigned short)(b& 0xf8)|(0x00);

	fch=(unsigned short) (0xff)&( r | (g1 >> 5) );
	fcl=(unsigned short) (0xff)&((g2 << 3) | (b >> 3) );
}


