void drawLine(int x1, int y1, int x2, int y2){
	if (y1==y2)
		drawHLine(x1, y1, x2-x1);
	else if (x1==x2)
		drawVLine(x1, y1, y2-y1);
	else{
		unsigned int	dx = (x2 > x1 ? x2 - x1 : x1 - x2);
		short			xstep =  x2 > x1 ? 1 : -1;
		unsigned int	dy = (y2 > y1 ? y2 - y1 : y1 - y2);
		short			ystep =  y2 > y1 ? 1 : -1;
		int				col = x1, row = y1;

		CS(0);
		if (dx < dy)
		{
			int t = - (dy >> 1);
			while (true){
				setXY(col, row, col, row);
				LCD_Writ_BusData8(fch, fcl);
				if (row == y2)
					return;
				row += ystep;
				t += dx;
				if (t >= 0){
					col += xstep;
					t   -= dy;
				}
			} 
		}
		else{
			int t = - (dx >> 1);
			while (true){
				setXY (col, row, col, row);
				LCD_Writ_BusData8(fch, fcl);
				if (col == x2)
					return;
				col += xstep;
				t += dy;
				if (t >= 0){
					row += ystep;
					t   -= dx;
				}
			} 
		}
		CS(1);
	}
	clrXY();
}




